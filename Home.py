import streamlit as st 

st.title('한줄로 웹사이트 만들기 (feat. Streamlit)')
st.header('소개')
st.write("""
이 페이지는 2023년 재독과협 파이썬 코딩캠프에서 마련된 특별강좌 Streamlit 튜토리얼에서 제작한 페이지입니다. 
강의에서 다룬 내용 이외에도 시간이 부족하여 미처 다루지 못한 내용을 함께 포함시켜 등록해두었습니다. 
해당 코드의 데모는 https://streamcamp.streamlit.app 주소로 접속해서 확인할 수 있습니다.
코드를 참고하여 여러분의 연구/실험 결과를 직접 분석하고, 자신만의 포트폴리오를 만들어 웹에 배포해 보세요.
""")

st.header('Streamlit')
st.write("""
[스트림릿](https://streamlit.io)은 데이터 시각화를 위한 파이썬 패키지로, 데이터 분석 결과를 손쉽게 웹 브라우저에서 볼 수 있도록 해줍니다.
프론트엔드 개발에 대한 지식 없이 파이썬 코드만으로도 시각적으로 아름답고 모바일 환경에서도 완변하게 동작하는 반응형 웹페이지를 만들 수 있습니다. 
이 라이브러리를 활용하면 데이터 분석 뿐 아니라 간단한 웹 앱을 제작할 수도 있습니다. 
또한 개발사가 함께 제공하는 클라우드 서비스를 이용하여 간편하게 웹으로 배포할 수도 있습니다. 

본 튜토리얼에서 제작한 앱은 아래와 같습니다.

 * 메모장 : 작성한 메모를 txt 파일로 저장하고 다운받을 수 있는 간단한 웹 메모장입니다.
 * 스팸조회 : 전화번호, 이메일 혹은 IP 주소가 스팸리스트에 등록되어 있는지 조회합니다.
 * IP 검색 : 입력한 IPv4 주소의 할당 국가를 조회합니다.
 * IP 검색 [중급] : 입력한 IPv4 주소의 지리정보를 조회합니다.
 * 편의점 데이터 분석 : 경기도에서 제공하는 편의점 데이터를 분석한 페이지입니다.
""")

st.header('설치')
st.write("""
소스코드를 다운로드 받아서 로컬 환경에서 직접 코드를 수정하거나 새로운 기능을 추가할 수 있습니다. 
직접 테스트해보고 싶다면 아래 절차에 따라 코드를 실행해보세요.

1. 깃허브에서 소스 코드를 다운로드 받으세요.
```
git clone https://github.com/seungh/streamcamp.git
```

2. 다운로드 받은 소스코드 폴더로 이동합니다.
```
cd streamcamp
```

3. 프로그램에서 사용하는 추가 패키지를 설치합니다. streamlit 이외에도 pandas와 plotyly 패키지를 사용하므로 `pip install streamlit pandas plotly` 
명령으로 패키지를 설치할 수 있습니다. 하지만 버전이 업데이트됨에 따라서 오류가 발생할 수 있는데 이를 피하고 싶다면 아래 명령어를 이용해서 설치하세요. 
소스코드와 함께 등록되어 있는 `requirements.txt` 파일에는 사용된 라이브러리의 버전이 지정되어 있습니다. 
```
pip install -r requirements.txt
```

4. 다운로드 받은 코드를 실행합니다.
```
streamlit run Home.py
```

5. 웹 브라우저의 주소창에 `http://localhost:8501`을 입력하여 작성한 코드가 어떻게 동작하는지 확인할 수 있습니다. 
자신만의 프로그램을 완성하여 [깃허브](https://github.com)와 [스트림릿 클라우드](https://share.streamlit.io)를 이용해서 웹으로 배포해보세요.

""")

